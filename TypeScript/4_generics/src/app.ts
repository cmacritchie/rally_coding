interface CourseGoal {
  title: string;
  description: string;
  completeUntil: Date;
}

function createCourseGoal(
  title: string,
  description: string,
  date: Date
): CourseGoal {
  let courseGoal: Partial<CourseGoal> = {};  //PArtial means all the properties are optional which is good for an empty object
  courseGoal.title = title;                 //Sometimes you may temporarily want to change a type
  courseGoal.description = description;
  courseGoal.completeUntil = date;
  return courseGoal as CourseGoal; //at the end we set it as Course Goal so that 
}

